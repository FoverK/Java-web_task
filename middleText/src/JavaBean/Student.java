package JavaBean;

public class Student {
		private String name;
		private String password;
		public void setName(String name) {
			this.name = name;
		}
		public void setPassword(String pwd) {
			this.password = pwd;
		}
		public String getName() {
			return name;
		}
		public String getPassword() {
			return password;
		}
}
